class Lollipop {
    public static void unwrap() { 
	System.out.println("Lollipop unwrapped"); 
	}
    public static void lick() {
		System.out.println("Licking the lollipop");
		
		}
    public static void bite() {
		System.out.println("Biting the lollipop"); 
		}
    public static void finish() {
		System.out.println("Lollipop finished");
	}
    public static void flavor() {
		System.out.println("Lollipop flavor is strawberry");
		}
    public static void changeFlavor() {
		unwrap();
		lick();
		bite();
		finish();
		flavor();		
	System.out.println("Changing lollipop flavor"); 
	}
    public static void share() {
		System.out.println("Sharing the lollipop"); 
		}
    public static void drop() {
		System.out.println("Lollipop dropped");
		}
    public static void buy() {
		System.out.println("Buying a lollipop"); 
		}
    public static void smell() {
		System.out.println("Smelling the lollipop");
		
		}
    
   
}
