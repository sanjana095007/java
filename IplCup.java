class IplCup {
    public static void startTournament() 
	{ 
	System.out.println("IPL tournament started");
	}
    public static void endTournament() 
	{
		System.out.println("IPL tournament ended"); 
		}
    public static void awardCup()
	{
		System.out.println("IPL Cup awarded");
		}
    public static void announceMVP()
	{
		System.out.println("MVP announced"); 
		}
    public static void celebrateVictory() 
	{
		System.out.println("Victory celebration");
		}
    public static void conductOpeningCeremony()
	{
		System.out.println("Opening ceremony conducted");
		startTournament();
        endTournament();
		awardCup();
        announceMVP();
        celebrateVictory();		
		}
    public static void sellTickets()
	{
		System.out.println("Selling IPL tickets");
		}
    public static void setBroadcastingRights() 
	{
		System.out.println("Broadcasting rights assigned"); 
		}
    public static void releaseMatchSchedule()
	{
		System.out.println("Match schedule released"); 
		}
    public static void organizeClosingCeremony() 
	{
		System.out.println("Closing ceremony organized"); 
		}
}


