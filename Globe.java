class Globe {
    public static void rotate() 
	{
		System.out.println("Globe rotating"); 
		}
    public static void showCountries()
	{
		System.out.println("Displaying countries"); 
		}
    public static void zoomIn() 
	{
		System.out.println("Zooming in on the globe"); 
		}
    public static void zoomOut() 
	{
		System.out.println("Zooming out on the globe"); 
		}
    public static void displayEarth()
	{
		System.out.println("Showing entire Earth"); 
		}
    public static void showContinents()
	{
		System.out.println("Displaying continents"); 
		}
    public static void showOceans()
	{
		System.out.println("Displaying oceans"); 
		rotate();
        showCountries();
        zoomIn();
       zoomOut();
        displayEarth();
		}
    public static void markLocation() 
	{
		System.out.println("Marking specific location"); 
		}
    public static void highlightCountry() 
	{
		System.out.println("Highlighting a country");
		}
    public static void spin() 
	{
		System.out.println("Spinning the globe"); 
		}
}


